import io
from PIL import Image, ImageDraw
from typing import Dict, Any
from app.services.storage_service import storage_service


class EvaluationReportGenerator:
    @staticmethod
    async def generate_and_save_report(
        run_id: str,
        metrics: Dict[str, Any],
        model_name: str,
        hyperparameters: Dict[str, Any]
    ) -> Dict[str, str]:
        """
        Generate and save evaluation report artifacts:
        1. Confusion Matrix PNG plot using Pillow (bypassing Matplotlib DLL loading).
        2. Markdown report text.
        Returns a dictionary mapping artifact type to its relative storage path.
        """
        # 1. Generate Confusion Matrix PNG via Pillow
        cm = metrics.get("confusion_matrix", {"tn": 0, "fp": 0, "fn": 0, "tp": 0})
        tn, fp, fn, tp = cm["tn"], cm["fp"], cm["fn"], cm["tp"]

        # Create image 400x400 with light background
        img = Image.new("RGB", (400, 400), color=(245, 245, 245))
        draw = ImageDraw.Draw(img)

        # Draw Title
        draw.text((20, 20), f"Confusion Matrix - {model_name.upper()}", fill=(20, 20, 20))

        # Column labels
        draw.text((120, 70), "Pred Non-Fire", fill=(50, 50, 50))
        draw.text((270, 70), "Pred Fire", fill=(50, 50, 50))

        # Row labels
        draw.text((20, 150), "Non-Fire", fill=(50, 50, 50))
        draw.text((20, 290), "True Fire", fill=(50, 50, 50))

        # Grid box coordinates
        # Box 1 (TN): (100, 100) to (230, 230) -> light peach
        draw.rectangle([100, 100, 230, 230], fill=(255, 220, 200), outline=(200, 100, 50))
        draw.text((140, 155), f"TN: {tn}", fill=(100, 50, 10))

        # Box 2 (FP): (240, 100) to (370, 230) -> light gray
        draw.rectangle([240, 100, 370, 230], fill=(230, 230, 230), outline=(150, 150, 150))
        draw.text((280, 155), f"FP: {fp}", fill=(80, 80, 80))

        # Box 3 (FN): (100, 240) to (230, 370) -> light gray
        draw.rectangle([100, 240, 230, 370], fill=(230, 230, 230), outline=(150, 150, 150))
        draw.text((140, 295), f"FN: {fn}", fill=(80, 80, 80))

        # Box 4 (TP): (240, 240) to (370, 370) -> fire orange
        draw.rectangle([240, 240, 370, 370], fill=(255, 140, 0), outline=(139, 69, 19))
        draw.text((280, 295), f"TP: {tp}", fill=(255, 255, 255))

        png_buf = io.BytesIO()
        img.save(png_buf, format="PNG")

        cm_path = f"runs/{run_id}/artifacts/confusion_matrix.png"
        await storage_service.save_file(png_buf.getvalue(), cm_path)

        # 2. Markdown Report
        report_md = f"""# Model Evaluation Report

## Run Context
*   **Run ID**: `{run_id}`
*   **Model Architecture**: `{model_name}`
*   **Hyperparameters**:
    *   Learning Rate: `{hyperparameters.get("learning_rate")}`
    *   Batch Size: `{hyperparameters.get("batch_size")}`
    *   Epochs: `{hyperparameters.get("epochs")}`
    *   Optimizer: `{hyperparameters.get("optimizer")}`
    *   Dropout: `{hyperparameters.get("dropout")}`
    *   Weight Decay: `{hyperparameters.get("weight_decay")}`

## Metrics Summary
*   **Accuracy**: `{metrics.get("accuracy", 0.0) * 100:.2f}%`
*   **Precision**: `{metrics.get("precision", 0.0) * 100:.2f}%`
*   **Recall (Sensitivity)**: `{metrics.get("recall", 0.0) * 100:.2f}%`
*   **F1 Score**: `{metrics.get("f1_score", 0.0) * 100:.2f}%`
*   **ROC AUC**: `{metrics.get("roc_auc", 0.0) * 100:.2f}%`

## Confusion Matrix Breakdown
*   **True Negatives (TN)**: `{tn}` (Correctly identified Non-Fire)
*   **False Positives (FP)**: `{fp}` (Non-Fire misclassified as Fire)
*   **False Negatives (FN)**: `{fn}` (Fire misclassified as Non-Fire)
*   **True Positives (TP)**: `{tp}` (Correctly identified Fire)

---
*Report automatically generated by Forest Fire Detection CNN Pipeline Engine.*
"""
        md_path = f"runs/{run_id}/artifacts/evaluation_report.md"
        await storage_service.save_file(report_md.encode("utf-8"), md_path)

        return {
            "confusion_matrix": cm_path,
            "evaluation_report": md_path
        }


evaluation_report_generator = EvaluationReportGenerator()
